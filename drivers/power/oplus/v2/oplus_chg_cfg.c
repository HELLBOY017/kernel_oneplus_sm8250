static union { char c[4]; unsigned long l; } endian_test = { { 'l', '?', '?', 'b' } };
#define ENDIANNESS ((char)endian_test.l)

struct sdesc {
	struct shash_desc shash;
	char ctx[];
};

static struct sdesc *init_sdesc(struct crypto_shash *alg)
{
	struct sdesc *sdesc;
	int size;

	size = sizeof(struct shash_desc) + crypto_shash_descsize(alg);
	sdesc = kmalloc(size, GFP_KERNEL);
	if (!sdesc)
		return ERR_PTR(-ENOMEM);
	sdesc->shash.tfm = alg;
	return sdesc;
}

static int calc_hash(struct crypto_shash *alg, const unsigned char *data,
		     unsigned int datalen, unsigned char *digest)
{
	struct sdesc *sdesc;
	int ret;

	sdesc = init_sdesc(alg);
	if (IS_ERR(sdesc)) {
		chg_info("can't alloc sdesc\n");
		return PTR_ERR(sdesc);
	}

	ret = crypto_shash_digest(&sdesc->shash, data, datalen, digest);
	kfree(sdesc);
	return ret;
}

static u32 oplus_chg_cfg_get_data_size(struct oplus_chg_param_head *param_head)
{
	struct oplus_chg_cfg_data_head *data_head;
	u32 index;

	for (index = 0; index < param_head->size; ) {
		data_head = (struct oplus_chg_cfg_data_head *)(param_head->data + index);
		if (data_head->magic != OPLUS_CHG_CFG_MAGIC) {
			chg_err("data magic error\n");
			return 0;
		}
		index += data_head->size + sizeof(struct oplus_chg_cfg_data_head);
	}

	return index;
}

int oplus_chg_verify_signature(u8 *s, u32 s_size, u8 *digest, u8 digest_size)
{
	struct public_key_signature sig;

	sig.s = s;
	sig.s_size = s_size;
	sig.digest = digest;
	sig.digest_size = digest_size;
	sig.pkey_algo = "rsa";
	sig.hash_algo = "sha256";
	sig.encoding = "pkcs1";
#if (LINUX_VERSION_CODE >= KERNEL_VERSION(5, 10, 0))
	sig.data_size = 0;
	sig.data = NULL;
#endif

	return public_key_verify_signature(&oplus_chg_pauth_code, &sig);
}

int oplus_chg_check_cfg_data(u8 *buf, int buf_len)
{
	struct oplus_chg_cfg_head *cfg_head;
	struct oplus_chg_param_head *param_head;
	struct crypto_shash *alg;
	char *hash_alg_name = "sha256";
	unsigned char digest[32];
	int rc, tmp;

	if (ENDIANNESS == 'b') {
		chg_err("Big-endian mode is temporarily not supported\n");
		return -EINVAL;
	}

	if (buf == NULL) {
		chg_err("data buf is null\n");
		return -EINVAL;
	}
	if (buf_len < sizeof(struct oplus_chg_cfg_head)) {
		chg_err("buf_len(=%d) error\n", buf_len);
		return -EINVAL;
	}

	cfg_head = (struct oplus_chg_cfg_head *)buf;
	tmp = cfg_head->size + sizeof(struct oplus_chg_cfg_head);
	if (buf_len != tmp) {
		chg_err("buf_len(=%d) error, expected to be %d\n", buf_len, tmp);
		return -EINVAL;
	}


	if (cfg_head->magic != OPLUS_CHG_CFG_MAGIC ||
	    cfg_head->head_size != sizeof(struct oplus_chg_cfg_head)) {
		chg_err("cfg head error, magic=0x%08x, size=%d\n", cfg_head->magic, cfg_head->head_size);
		return -EINVAL;
	}

	param_head = (struct oplus_chg_param_head *)((unsigned char *)buf + cfg_head->param_index[OPLUS_CHG_WIRED_PARAM]);
	if (param_head->size != oplus_chg_cfg_get_data_size(param_head)) {
		chg_err("wired charge parameter length error, len=%d\n", oplus_chg_cfg_get_data_size(param_head));
		return -EINVAL;
	}
	param_head = (struct oplus_chg_param_head *)((unsigned char *)buf + cfg_head->param_index[OPLUS_CHG_WLS_PARAM]);
	if (param_head->size != oplus_chg_cfg_get_data_size(param_head)) {
		chg_err("wireless charge parameter length error, len=%d\n", oplus_chg_cfg_get_data_size(param_head));
		return -EINVAL;
	}
	param_head = (struct oplus_chg_param_head *)((unsigned char *)buf + cfg_head->param_index[OPLUS_CHG_COMM_PARAM]);
	if (param_head->size != oplus_chg_cfg_get_data_size(param_head)) {
		chg_err("common parameter length error, len=%d\n", oplus_chg_cfg_get_data_size(param_head));
		return -EINVAL;
	}
	param_head = (struct oplus_chg_param_head *)((unsigned char *)buf + cfg_head->param_index[OPLUS_CHG_VOOC_PARAM]);
	if (param_head->size != oplus_chg_cfg_get_data_size(param_head)) {
		chg_err("vooc parameter length error, len=%d\n", oplus_chg_cfg_get_data_size(param_head));
		return -EINVAL;
	}

	alg = crypto_alloc_shash(hash_alg_name, 0, 0);
	if (IS_ERR(alg)) {
		chg_err("can't alloc alg %s\n", hash_alg_name);
		return PTR_ERR(alg);
	}
	rc = calc_hash(alg, (unsigned char *)buf + cfg_head->head_size, cfg_head->size, digest);
	if (rc < 0) {
		chg_err("Configuration file digest calculation failed, rc=%d\n", rc);
		return rc;
	}
	crypto_free_shash(alg);

	rc = oplus_chg_verify_signature(cfg_head->signature, 512, digest, 32);
	if (rc < 0) {
		chg_err("Configuration file signature verification failed, rc=%d\n", rc);
		return rc;
	}

	return 0;
}

int oplus_chg_cfg_update_config(u8 *buf, int buf_len)
{
	struct oplus_mms *topic;
	struct oplus_chg_cfg_head *cfg_head;
	struct oplus_chg_param_head *param_head;
	int rc, tmp;

	if (buf == NULL) {
		chg_err("buf is NULL\n");
		return -EINVAL;
	}
	if (buf_len < sizeof(struct oplus_chg_cfg_head)) {
		chg_err("buf_len(=%d) error\n", buf_len);
		return -EINVAL;
	}

	cfg_head = (struct oplus_chg_cfg_head *)buf;
	tmp = cfg_head->size + sizeof(struct oplus_chg_cfg_head);
	if (buf_len != tmp) {
		chg_err("buf_len(=%d) error, expected to be %d\n", buf_len, tmp);
		return -EINVAL;
	}

	topic = oplus_mms_get_by_name("commmon");
	if (topic != NULL) {
		param_head =
			(struct oplus_chg_param_head
				 *)((u8 *)buf +
				    cfg_head->param_index[OPLUS_CHG_COMM_PARAM]);
		rc = oplus_comm_set_config(topic, param_head);
		if (rc < 0)
			return rc;
	}
	topic = oplus_mms_get_by_name("wired");
	if (topic != NULL) {
		param_head =
			(struct oplus_chg_param_head
				 *)((u8 *)buf +
				    cfg_head->param_index[OPLUS_CHG_WIRED_PARAM]);
		rc = oplus_wired_set_config(topic, param_head);
		if (rc < 0)
			return rc;
	}
	topic = oplus_mms_get_by_name("vooc");
	if (topic != NULL) {
		param_head =
			(struct oplus_chg_param_head
				 *)((u8 *)buf +
				    cfg_head->param_index[OPLUS_CHG_VOOC_PARAM]);
		rc = oplus_vooc_set_config(topic, param_head);
		if (rc < 0)
			return rc;
	}

	return 0;
}
